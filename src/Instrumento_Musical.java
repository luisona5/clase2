public class Instrumento_Musical {
    String nombre;
    String tipo;
    String tamano;
    int frecuencia;

    public Instrumento_Musical(String nombre, String tipo, String tamano, int frecuencia) {
        this.nombre = nombre;
        this.tipo = tipo;
        this.tamano = tamano;
        this.frecuencia = frecuencia;
    }

}
